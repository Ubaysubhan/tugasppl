package main;

public class Person {
    private String name;
    
    private Integer height;
    
    private Integer weight;
    
    public String getName() {
        return this.name;
    }
    
    public String setName(String name) {
        this.name = name;
    }
    
    public Integer getHeight() {
        return this.height;
    }
    
    public Integer setHeight(Integer height) {
        this.height = height;
    }
    
    public Integer getWeight() {
        return this.weight;
    }
    
    public Integer setWeight(Integer weight) {
        this.weight = weight;
    }
}
